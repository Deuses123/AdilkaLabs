package com.example.untitled;

public class lab4 {
    public static void main(String[] args) {
        String[] hexValues = {"1A", "FF", "8", "B4", "14E"};

        for (String hex : hexValues) {
            int decimalValue = hexToDecimal(hex);
            System.out.println("Шестнадцатеричное значение " + hex + " в десятичной системе: " + decimalValue);
        }
    }

    public static int hexToDecimal(String hex) {
        // Преобразование шестнадцатеричной строки в десятичное число
        int decimalValue = 0;
        for (int i = 0; i < hex.length(); i++) {
            char c = hex.charAt(i);
            int digit = Character.digit(c, 16); // Получение числового значения символа в 16-ричной системе
            decimalValue = decimalValue * 16 + digit;
        }
        return decimalValue;
    }
}



