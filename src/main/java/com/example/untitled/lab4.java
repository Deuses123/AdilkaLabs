//
//        Извлеките символы числа справа налево.
//        Каждый символ, начиная справа, имеет свой вес в 16-ричной системе счисления: 16^0, 16^1, 16^2 и так далее.
//        Преобразуйте каждый символ в его десятичное представление (цифры от 0 до 15). Для этого используется метод Character.digit(c, 16), где c - это символ в 16-ричной системе.
//        Пример:
//
//        Допустим, у вас есть 16-ричное число "1A". Преобразование будет следующим:
//
//        Извлекаем символы справа налево: 'A' и '1'.
//        Вес 'A' равен 16^0 (1), а вес '1' равен 16^1 (16).
//        Преобразуем символ 'A' в его десятичное представление: Character.digit('A', 16) вернет 10.
//        Теперь мы можем вычислить десятичное значение: (10 * 1) + (1 * 16) = 10 + 16 = 26.
//        Таким образом, "1A" в 16-ричной системе эквивалентно числу 26 в десятичной системе.